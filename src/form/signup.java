    private void btnSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignActionPerformed
        // TODO add your handling code here:
        String id_adminText = txtID.getText().trim();
        String password = new String(txtPass.getPassword());
        String confirmPassword = new String(txtConfpass.getPassword());
        String namaText = txtNama.getText().trim();

        if (id_adminText.isEmpty() || password.isEmpty() || confirmPassword.isEmpty() || namaText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap isi semua kolom.");
            return;
        }
        if (id_adminText.length() > 15) {
            JOptionPane.showMessageDialog(this, "ID Admin harus terdiri dari 15 digit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (id_admTerdaftar(id_adminText)) {
            JOptionPane.showMessageDialog(this, "ID Admin sudah terdaftar.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Password dan Konfirmasi Password tidak cocok.");
            return;
        }

        String sql = "INSERT INTO tb_login (id_admin, password, nama) VALUES (?, ?, ?)";
        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, id_adminText);
            pstmt.setString(2, password);
            pstmt.setString(3, namaText);

            int rowsAffected = pstmt.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Daftar berhasil.");
                this.dispose();
                login loginForm = new login();
                loginForm.setVisible(true);
                loginForm.setLocationRelativeTo(null);
            } else {
                JOptionPane.showMessageDialog(this, "Daftar gagal.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            Logger.getLogger(signup.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnSignActionPerformed

