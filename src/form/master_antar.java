    protected void datatable() {
        Object[] Baris = {"Kode", "Tipe", "Biaya Tambahan (Rp)"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txCari.getText();

        try {
            String sql = "SELECT * FROM tb_antar WHERE kode_antar LIKE '%" + cariitem + "%' "
                    + "OR tipe LIKE '%" + cariitem + "%' "
                    + "OR biaya LIKE '%" + cariitem + "%' "
                    + "ORDER BY kode_antar ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),});
            }
            tblAntar.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }
        
        protected void autonumber() {
        try {
            String sql = "SELECT kode_antar from tb_antar order by kode_antar asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txID.setText("A01");
            while (rs.next()) {
                String kodeantar = rs.getString("kode_antar").substring(2);
                int AN = Integer.parseInt(kodeantar) + 1;
                String Nol = "";
                if (AN < 10) {
                    Nol = "0";
                }
                txID.setText("A" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Number Gagal" + e);
        }
    }

