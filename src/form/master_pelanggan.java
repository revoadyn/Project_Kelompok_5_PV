            stat.setString(1, id_pelangganText);
            stat.setString(2, namaText);
            stat.setString(3, jenis);
            stat.setString(4, notelpText);
            stat.setString(5, jenispel);
            stat.setString(6, alamatText);

            stat.executeUpdate();
