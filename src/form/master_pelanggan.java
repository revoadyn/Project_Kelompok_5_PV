    protected void datatable() {
        Object[] Baris = {"ID Pelanggan", "Nama Lengkap", "Jenis Kelamin", "No Telepon", "Jenis Pelanggan", "Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txCari.getText();

        try {
            String sql = "SELECT * FROM tb_pelanggan WHERE id_pelanggan LIKE '%" + cariitem + "%' "
                    + "OR nama LIKE '%" + cariitem + "%' "
                    + "OR jk LIKE '%" + cariitem + "%' "
                    + "OR no_telp LIKE '%" + cariitem + "%' "
                    + "OR jenis_pelanggan LIKE '%" + cariitem + "%' "
                    + "OR alamat LIKE '%" + cariitem + "%' "
                    + "ORDER BY id_pelanggan ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),});
            }
            tblPelanggan.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil" + e);
        }
    }
        
        protected void autonumber() {
        try {
            String sql = "SELECT id_pelanggan from tb_pelanggan order by id_pelanggan asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txID.setText("IP0001");
            while (rs.next()) {
                String idpelanggan = rs.getString("id_pelanggan").substring(2);
                int AN = Integer.parseInt(idpelanggan) + 1;
                String Nol = "";
                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }
                txID.setText("IP" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Number Gagal" + e);
        }
    }
