    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String id_pelangganText = txID.getText().trim(); // Trim untuk menghapus spasi di awal dan akhir
        String namaText = txNama.getText().trim();
        String jenis = null;
        if(rLaki.isSelected()){
            jenis = "Laki - laki";
        }else if(rPrmpn.isSelected()){
            jenis = "Perempuan";
        }
        String notelpText = txNoTelp.getText().trim();
        String jenispel = cbJenispel.getSelectedItem().toString();
        String alamatText = txAlamat.getText().trim();
        if (namaText.isEmpty() || notelpText.isEmpty() || alamatText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jenis.equals("Pilih Jenis Kelamin")) {
            JOptionPane.showMessageDialog(this, "Jenis Kelamin harus dipilih.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jenispel.equals("Pilih Jenis Pelanggan")) {
            JOptionPane.showMessageDialog(this, "Jenis Pelanggan harus dipilih.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "UPDATE tb_pelanggan SET nama=?, jk=?, no_telp=?, jenis_pelanggan=?, alamat=? WHERE id_pelanggan=?"; // Perhatikan penggunaan UPDATE
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, namaText);
            stat.setString(2, jenis);
            stat.setString(3, notelpText);
            stat.setString(4, jenispel);
            stat.setString(5, alamatText);
            stat.setString(6, id_pelangganText);
            int rowsUpdated = stat.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                kosong();
                aktif();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah!" + e, "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
        datatable();
    }//GEN-LAST:event_btnUbahActionPerformed

