    private void bprint_pelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bprint_pelActionPerformed
        try {
            String loginId = UserID.getIDAdmin();
            String loginAdmin = "Tidak Diketahui";

            try (PreparedStatement admnama = conn.prepareStatement("SELECT nama FROM tb_login WHERE id_admin = ?")) {
                admnama.setString(1, loginId);
                try (ResultSet rsNama = admnama.executeQuery()) {
                    if (rsNama.next()) {
                        loginAdmin = rsNama.getString("nama");
                    }
                }
            }

            String reportPath = "./src/ireport/report_admin.jasper";
            HashMap parameter = new HashMap();

            JasperPrint print = JasperFillManager.fillReport(reportPath,parameter,conn);
            JasperViewer.viewReport(print,false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak report: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_bprint_pelActionPerformed

