    protected void datatable_admin() {
        Object[] Baris = {"ID Admin", "Password", "Nama"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = cari_admin.getText();

        try {
            String sql = "SELECT * FROM tb_login WHERE id_admin LIKE '%" + cariitem + "%' "
                    + "OR password LIKE '%" + cariitem + "%' "
                    + "OR nama LIKE '%" + cariitem + "%' "
                    + "ORDER BY id_admin ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                });
            }
            tblAdmin.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil" + e);
        }
    }

